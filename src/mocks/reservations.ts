export type ReservationState = 'waiting' | 'completed' | 'cancelled';

export interface Reservation {
  id: string;
  /** 이 예약이 속한 부스 id. 화면 필터링 · 권한 검사 기준. */
  boothId: number;
  /** 부스 대기열 접수 번호. 대기 순번 정렬·표시 기준. */
  reservationNumber: number;
  name: string;
  people: number;
  contact: string;
  status: ReservationState;
}

